        //Filme filmeDaNara   = new Filme();
        //filmeDaNara.setNome("The Seventh Seal");
        filmeDaNara.setDiretor("Ingmar Bergman");
        filmeDaNara.setAnoDeLancamento(1957);
        filmeDaNara.setDuracaoEmMinutos(96);
        filmeDaNara.avalia(10);

        ArrayList<Filme> listaDeFilmes = new ArrayList<>();
        listaDeFilmes.add(filmeDaNara);
        listaDeFilmes.add(meuFilme);
        listaDeFilmes.add(outroFilme);
        System.out.println("Tamanho da lista: " + listaDeFilmes.size());
        System.out.println("Primeiro filme: " + listaDeFilmes.get(0).getNome());
        System.out.println(listaDeFilmes);
        System.out.println("Segundo filme: " + listaDeFilmes.get(1).getNome());
        System.out.println("Terceiro filme: " + listaDeFilmes.get(2).getNome());


